package sc.app.smartadapter.configuration;

public class ClassAdapterConfiguration {

	String source_bean;
	String target_bean;

	public String getSource_bean() {
		return source_bean;
	}

	public void setSource_bean(String source_bean) {
		this.source_bean = source_bean;
	}

	public String getTarget_bean() {
		return target_bean;
	}

	public void setTarget_bean(String target_bean) {
		this.target_bean = target_bean;
	}

}
