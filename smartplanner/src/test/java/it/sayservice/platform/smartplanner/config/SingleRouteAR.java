package it.sayservice.platform.smartplanner.config;

public class SingleRouteAR {
  String routeId;
  String routeSymId;
  String title;
  String label;
	public String getRouteId() {
		return routeId;
	}
	public void setRouteId(String routeId) {
		this.routeId = routeId;
	}
	public String getRouteSymId() {
		return routeSymId;
	}
	public void setRouteSymId(String routeSymId) {
		this.routeSymId = routeSymId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
}
